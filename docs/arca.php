 <br /><br>
<p>На следующем этапе Вам необходимо будет закрепить направляющие на боковых и верхней поверхностях проема,с шагом 15-20 см. Это можно сделать с помощью шурупов или дюбелей.<img src="img/arka_3.jpg" alt="arka_3" width="300" height="190" align="right" class="img-responsive"/><br />
