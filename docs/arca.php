                <p class="lead">Арка из гипсокартона – это один из способов изменить интерьер вашей квартиры. Чаще всего арки устанавливаются в проемах. Для того чтобы изготовить арку самостоятельно потребуется небольшой опыт, некоторый инструмент и немного терпения.</span></p>
<p>Для начала необходимо определиться с радиусом арки. В каждом случае радиус подбирается индивидуально.<br />
